public class Orientation {
    private double W;
    private double X;
    private double Y;
    private double Z;

    public Orientation(double W, double X, double Y, double Z) {
        this.W = W;
        this.X = X;
        this.Y = Y;
        this.Z = Z;
    }

    public double getW() {
        return W;
    }

    public void setW(double W) {
        this.W = W;
    }

    public double getX() {
        return X;
    }

    public void setX(double X) {
        this.X = X;
    }

    public double getY() {
        return Y;
    }

    public void setY(double Y) {
        this.Y = Y;
    }

    public double getZ() {
        return Z;
    }

    public void setZ(double Z) {
        this.Z = Z;
    }
}
